// Helper functions
export const getTeamColors = (teamCode) => {
  return TEAM_COLORS[teamCode?.toUpperCase()] || TEAM_COLORS.default;
};

export const getTeamGradient = (teamCode) => {
  const colors = getTeamColors(teamCode);
  return colors.gradient;
};

export const getTeamPrimary = (teamCode) => {
  const colors = getTeamColors(teamCode);
  return colors.primary;
};

export const getTeamSecondary = (teamCode) => {
  const colors = getTeamColors(teamCode);
  return colors.secondary;
};

export const getTeamAccent = (teamCode) => {
  const colors = getTeamColors(teamCode);
  return colors.accent;
};

// CSS Custom Properties Generator
export const generateTeamCSS = (teamCode) => {
  const colors = getTeamColors(teamCode);
  return {
    '--team-primary': colors.primary,
    '--team-secondary': colors.secondary,
    '--team-accent': colors.accent,
    '--team-gradient': colors.gradient
  };
};

// Division mappings for additional organization
export const DIVISIONS = {
  'AL East': ['BAL', 'BOS', 'NYY', 'TB', 'TOR'],
  'AL Central': ['CWS', 'CLE', 'DET', 'KC', 'MIN'],
  'AL West': ['HOU', 'LAA', 'OAK', 'SEA', 'TEX'],
  'NL East': ['ATL', 'MIA', 'NYM', 'PHI', 'WSH'],
  'NL Central': ['CHC', 'CIN', 'MIL', 'PIT', 'STL'],
  'NL West': ['ARI', 'COL', 'LAD', 'SD', 'SF']
};

export const getTeamDivision = (teamCode) => {
  for (const [division, teams] of Object.entries(DIVISIONS)) {
    if (teams.includes(teamCode?.toUpperCase())) {
      return division;
    }
  }
  return 'Unknown';
};
